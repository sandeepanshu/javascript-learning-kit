// Bubble sort
// Worst case time complexity = O(n^2)
// When will owrst case happen? Reverse sorted
// Best case time complexity = Omega(n)
// When will best case happen? already sorted
// Stable? YES
// In place YES

// Selection sort
// Worst case time complexity = O(n^2)
// Best case time complexity = O(n^2)
// Stable? No
// In place - YES 

// Insertion sort
// Worst case time complexity = O(n^2)
// Best case time complexity = Omega(n)
// when does best casse occur = already sorted
// when does worst case occur = reverse sorted
// Stable? YES
// In place? YES


